package main

import (
	"fmt"
	"sort"
	"strings"
	"time"
)

// reportBucket is the width of each bucket in the per-scenario time-bucketed table.
const reportBucket = 10 * time.Second

// resultsReport renders a simulation results set as a Markdown report.
type resultsReport struct {
	results scenarioResults
}

func newResultsReport(r scenarioResults) *resultsReport {
	return &resultsReport{results: r}
}

// generateMarkdown renders the full comparison report.
func (rr *resultsReport) generateMarkdown() string {
	var b strings.Builder
	b.WriteString("# wgo vs kgo simulation report\n\n")
	b.WriteString("_Generated by `make simulation`. Non-deterministic snapshot: latencies are " +
		"real wall-clock, so the exact numbers vary between runs._\n\n")
	fmt.Fprintf(&b, "- Cluster: %d brokers / %d partitions (1:1)\n", clusterSize, clusterSize)
	fmt.Fprintf(&b, "- Warmup %s, observed %s, event spacing %s, app-request timeout %s\n\n",
		warmupDuration, observedDuration, eventSpacing, appRequestTimeout)

	b.WriteString("## Summary\n\n")
	b.WriteString("| Scenario | wgo success | kgo success | wgo p99 | kgo p99 | hedge surge |\n")
	b.WriteString("| --- | --- | --- | --- | --- | --- |\n")
	for _, res := range rr.results.entries {
		fmt.Fprintf(&b, "| %s | %s | %s | %s | %s | %s |\n",
			res.sc.name,
			rr.appSuccessCell(res.wgoSummary), rr.appSuccessCell(res.kgoSummary),
			rr.formatDuration(res.wgoSummary.p99Latency), rr.formatDuration(res.kgoSummary.p99Latency),
			rr.hedgeSurgePct(res.totalPrimary, res.totalHedge))
	}
	b.WriteString("\n")

	for _, res := range rr.results.entries {
		rr.writeScenarioSection(&b, res)
	}
	return b.String()
}

func (rr *resultsReport) writeScenarioSection(b *strings.Builder, res scenarioResult) {
	fmt.Fprintf(b, "## %s\n\n%s\n\n", res.sc.name, res.sc.description)
	fmt.Fprintf(b, "- app requests (wgo): %s\n", rr.summaryLine(res.wgoSummary))
	fmt.Fprintf(b, "- app requests (kgo): %s\n", rr.summaryLine(res.kgoSummary))
	if res.totalPrimary > 0 {
		fmt.Fprintf(b, "- hedge surge: %d primary + %d hedge wire requests (%s extra)\n",
			res.totalPrimary, res.totalHedge, rr.hedgeSurgePct(res.totalPrimary, res.totalHedge))
	}
	b.WriteString("\n")

	rr.writeBucketTable(b, res)
	rr.writeErrorTable(b, res)
	b.WriteString("\n")
}

func (rr *resultsReport) writeErrorTable(b *strings.Builder, res scenarioResult) {
	if len(res.wgoErrorCounts) == 0 && len(res.kgoErrorCounts) == 0 {
		return
	}

	seen := map[string]struct{}{}
	for e := range res.wgoErrorCounts {
		seen[e] = struct{}{}
	}
	for e := range res.kgoErrorCounts {
		seen[e] = struct{}{}
	}
	errs := make([]string, 0, len(seen))
	for e := range seen {
		errs = append(errs, e)
	}
	sort.Strings(errs)

	b.WriteString("\n| error | wgo | kgo |\n| --- | --- | --- |\n")
	for _, e := range errs {
		fmt.Fprintf(b, "| %s | %s | %s |\n",
			e,
			rr.errorRatePct(res.wgoErrorCounts[e], res.wgoSummary.total),
			rr.errorRatePct(res.kgoErrorCounts[e], res.kgoSummary.total))
	}
}

func (rr *resultsReport) writeBucketTable(b *strings.Builder, res scenarioResult) {
	wgoBuckets := res.wgoBuckets
	kgoBuckets := res.kgoBuckets
	n := len(wgoBuckets)
	if len(kgoBuckets) > n {
		n = len(kgoBuckets)
	}
	if n == 0 && len(res.produceDeltas) == 0 {
		b.WriteString("(no observations)\n")
		return
	}

	b.WriteString("| bucket | wgo success | wgo mean | wgo p99 | wgo surge (hedge/prim) | kgo success | kgo mean | kgo p99 |\n")
	b.WriteString("| --- | --- | --- | --- | --- | --- | --- | --- |\n")
	secs := int(reportBucket / time.Second)
	for i := 0; i < n; i++ {
		var wgo, kgo observationsSummary
		if i < len(wgoBuckets) {
			wgo = wgoBuckets[i]
		}
		if i < len(kgoBuckets) {
			kgo = kgoBuckets[i]
		}
		surge := "n/a"
		if i < len(res.produceDeltas) {
			surge = rr.bucketSurgeCell(res.produceDeltas[i])
		}
		fmt.Fprintf(b, "| %ds–%ds | %s | %s | %s | %s | %s | %s | %s |\n",
			i*secs, (i+1)*secs,
			rr.bucketSuccessCell(wgo), rr.formatDuration(wgo.meanLatency), rr.formatDuration(wgo.p99Latency),
			surge,
			rr.bucketSuccessCell(kgo), rr.formatDuration(kgo.meanLatency), rr.formatDuration(kgo.p99Latency))
	}
	if drain := len(res.produceDeltas) - n; drain > 0 {
		var d counterBucketDelta
		for _, x := range res.produceDeltas[n:] {
			d.primary += x.primary
			d.hedge += x.hedge
		}
		fmt.Fprintf(b, "| %ds+ drain | — | — | — | %s | — | — | — |\n", n*secs, rr.bucketSurgeCell(d))
	}
}

// formatDuration renders a duration with one decimal in seconds so cells are
// uniform width and easy to scan.
func (rr *resultsReport) formatDuration(d time.Duration) string {
	return fmt.Sprintf("%.1fs", d.Seconds())
}

// appSuccessCell renders an app-level success cell, e.g. "950/1000 (95.0%)".
func (rr *resultsReport) appSuccessCell(s observationsSummary) string {
	if s.total == 0 {
		return "n/a"
	}
	return fmt.Sprintf("%d/%d (%.1f%%)", s.successes, s.total, 100*float64(s.successes)/float64(s.total))
}

// summaryLine renders a one-line success/latency summary of a set of observations.
func (rr *resultsReport) summaryLine(s observationsSummary) string {
	rate := "n/a"
	if s.total > 0 {
		rate = fmt.Sprintf("%d/%d (%.1f%%)", s.successes, s.total, 100*float64(s.successes)/float64(s.total))
	}
	return fmt.Sprintf("success=%s mean=%s p50=%s p99=%s",
		rate, rr.formatDuration(s.meanLatency), rr.formatDuration(s.p50Latency), rr.formatDuration(s.p99Latency))
}

// hedgeSurgePct renders the overall hedge surge (hedge wire requests as a
// percentage of primary), e.g. "+12.3%", or "n/a" when no primary requests were sent.
func (rr *resultsReport) hedgeSurgePct(primary, hedge int64) string {
	if primary <= 0 {
		return "n/a"
	}
	return fmt.Sprintf("+%.1f%%", 100*float64(hedge)/float64(primary))
}

// errorRatePct renders count as a percentage of total, e.g. "0.8%".
func (rr *resultsReport) errorRatePct(count, total int) string {
	if total == 0 {
		return "n/a"
	}
	return fmt.Sprintf("%.1f%%", 100*float64(count)/float64(total))
}

// bucketSuccessCell renders a per-bucket success cell, e.g. "20/20 (100%)".
func (rr *resultsReport) bucketSuccessCell(s observationsSummary) string {
	if s.total == 0 {
		return "n/a"
	}
	return fmt.Sprintf("%d/%d (%.0f%%)", s.successes, s.total, 100*float64(s.successes)/float64(s.total))
}

// bucketSurgeCell renders one bucket's hedge/primary cell, e.g. "12/200 (+6%)".
func (rr *resultsReport) bucketSurgeCell(d counterBucketDelta) string {
	if d.primary <= 0 {
		return "n/a"
	}
	return fmt.Sprintf("%d/%d (+%.0f%%)", d.hedge, d.primary, 100*float64(d.hedge)/float64(d.primary))
}
